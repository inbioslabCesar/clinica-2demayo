<?php
session_set_cookie_params([
    'lifetime' => 0,
    'path' => '/',
    'domain' => '.clinica2demayo.com',
    'secure' => true,
    'httponly' => true,
    'samesite' => 'Lax',
]);
session_start();
// CORS para localhost y producción
$allowedOrigins = [
    'http://localhost:5173',
    'http://localhost:5174',
    'http://localhost:5175',
    'http://localhost:5176',
    'https://clinica2demayo.com',
    'https://www.clinica2demayo.com'
];
$origin = $_SERVER['HTTP_ORIGIN'] ?? '';
if (in_array($origin, $allowedOrigins)) {
    header('Access-Control-Allow-Origin: ' . $origin);
}
header('Access-Control-Allow-Credentials: true');
header('Access-Control-Allow-Headers: Content-Type, X-Requested-With');
header('Access-Control-Allow-Methods: POST, GET, OPTIONS, DELETE');
if ($_SERVER['REQUEST_METHOD'] === 'OPTIONS') {
    http_response_code(200);
    exit();
}

// Capturar errores fatales y enviar JSON con CORS
set_exception_handler(function($e) use ($origin, $allowedOrigins) {
    http_response_code(500);
    if (in_array($origin, $allowedOrigins)) {
        header('Access-Control-Allow-Origin: ' . $origin);
    }
    header('Access-Control-Allow-Credentials: true');
    header('Content-Type: application/json');
    echo json_encode(['success' => false, 'error' => 'Error del servidor: ' . $e->getMessage()]);
    exit();
});
header('Content-Type: application/json');
require_once 'config.php';

// Ranking de exámenes más solicitados
$sql = "SELECT el.id, el.nombre, el.categoria, COUNT(ol.id) AS veces_solicitado
    FROM examenes_laboratorio el
    LEFT JOIN ordenes_laboratorio ol ON FIND_IN_SET(el.id, ol.examenes) > 0
    WHERE el.activo = 1
    GROUP BY el.id, el.nombre, el.categoria
    ORDER BY veces_solicitado DESC, el.nombre ASC
    LIMIT 100";
$res = $conn->query($sql);
$ranking = [];
while ($row = $res->fetch_assoc()) {
    $ranking[] = $row;
}
echo json_encode(["success" => true, "ranking" => $ranking]);
?>
